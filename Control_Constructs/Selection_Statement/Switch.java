package Control_Constructs.Selection_Statement;

public class Switch
{
	public static void main(String[] args)
	{
		int a = 5;
		
		switch (a) 
		{
			case 0:
			{
				System.out.println("Given number is : zero");
				break;
			}
			case 1:
			{
				System.out.println("Given number is : one");
				break;
			}
			case 2:
			{
				System.out.println("Given number is : two");
				break;
			}
			case 3:
			{
				System.out.println("Given number is : three");
				break;
			}
			case 4:
			{
				System.out.println("Given number is : four");
				break;
			}
			case 5:
			{
				System.out.println("Given number is : five");
				break;
			}
			case 6:
			{
				System.out.println("Given number is : six");
				break;
			}
			case 7:
			{
				System.out.println("Given number is : seven");
				break;
			}
			case 8:
			{
				System.out.println("Given number is : eigth");
				break;
			}
			case 9:
			{
				System.out.println("Given number is : nign");
				break;
			}
			default:
			{
				System.out.println("Somthing went wrong :(");
				break;
			}
		}
	}
}
