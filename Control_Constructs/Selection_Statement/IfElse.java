package Control_Constructs.Selection_Statement;

public class IfElse
{
	public static void main(String[] args)
	{
		int a=1;
		int b=5;
		
		if( a < b)
		{
			System.out.println( a + " is smaller than " + b);
		}
		else
		{
			System.out.println( a + " is grater than " + b);
		}
	}
}
